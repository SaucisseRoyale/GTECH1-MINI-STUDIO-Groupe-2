
import pygame
from pygame.locals import *

pygame.init()

screen_width = 1000
screen_height = 700
tile_size = 50
move_speed = 1


screen = pygame.display.set_mode((screen_width, screen_height))
pygame.display.set_caption('Platformer')

bg_img = pygame.image.load("sky-blue-color-solid-background-1920x1080.png").convert()

class World:
    def __init__(self, data):
        self.tile_list = []
        dirt_img = pygame.image.load("dirt.png").convert_alpha()
        grass_img = pygame.image.load("grass.png").convert_alpha()

        row_count = 0
        for row in data:
            col_count = 0
            for tile in row:
                if tile == 1:
                    img = pygame.transform.scale(dirt_img, (tile_size, tile_size))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * tile_size
                    img_rect.y = row_count * tile_size
                    self.tile_list.append((img, img_rect))
                elif tile == 2:
                    img = pygame.transform.scale(grass_img, (tile_size, tile_size))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * tile_size
                    img_rect.y = row_count * tile_size
                    self.tile_list.append((img, img_rect))
                col_count += 1
            row_count += 1

    def draw(self):
        for tile in self.tile_list:
            screen.blit(tile[0], tile[1])

class Player:
    def __init__(self, x, y):
        img = pygame.image.load("tile_0000.png").convert_alpha()
        self.image = pygame.transform.scale(img, (56, 56))
        self.rect = self.image.get_rect()
        self.rect.x = x
        self.rect.y = y
        self.vel_y : float= 0.0
        self.jumped = False

    def update(self):
        dx = 0
        dy = 0

        key = pygame.key.get_pressed()
        if key[K_LEFT]:
            dx -= move_speed
        if key[K_RIGHT]:
            dx += move_speed

        # Initier le saut
        if key[K_SPACE] and not self.jumped:
            self.vel_y = -2
            self.jumped = True
        if not key[K_SPACE]:
            self.jumped = False

        # Appliquer la gravité
        self.vel_y += 1 * 0.012 
        if self.vel_y > 10:  
            self.vel_y = 10

        dy += self.vel_y

        # Vérifier le contact avec le sol
        if self.rect.bottom + dy > screen_height:
            dy = screen_height - self.rect.bottom
            self.vel_y = 0 
            self.jumped = False

        # Mise à jour des coordonnées du joueur
        self.rect.x += dx
        self.rect.y += dy


        screen.blit(self.image, self.rect)


